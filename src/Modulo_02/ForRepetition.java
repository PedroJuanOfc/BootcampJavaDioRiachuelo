package Modulo_02;

public class ForRepetition {
    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }

//        Sintaxe geral:
//        for (inicialização; condição; incremento/decremento) {
//              // bloco de código a ser repetido
//        }
//        - Inicialização: ocorre apenas uma vez no início do loop (ex: int i = 1)
//        - Condição: enquanto for verdadeira, o bloco continua executando (ex: i <= 100)
//        - Incremento/Decremento: executado a cada iteração, após o bloco de código (ex: i++)
    }
}
